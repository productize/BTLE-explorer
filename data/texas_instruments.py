def ti_print_ir_temp(data):
  ambient = (data[2] + 256*data[3])/128.0
  # TODO target temp
  return "%0.2f" % (ambient)

def ti_print_accel(data):
  x = data[0] / 64.0
  y = data[1] / 64.0
  z = data[2] / -64.0
  return "X:%f,Y:%f,Z:%f" % (x,y,z)

